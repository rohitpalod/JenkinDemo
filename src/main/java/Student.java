public class Student {

    private String name ;
    private String collegeId;

    public void setCollegeId(String collegeId) {
        this.collegeId = collegeId;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCollegeId() {
        return collegeId;
    }

    public String getName() {
        return name;
    }
}
